    private void btnDeletarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeletarActionPerformed
        if (jTable1.getSelectedRow() != 1) {
            ClienteDAO dao = new ClienteDAO();
            Cliente c = new Cliente();
            c.setId(Integer.parseInt(jTable1.getValueAt(jTable1.getSelectedRow(), 0).toString()));
            dao.delete(c);
            readJTable();
        }
    }//GEN-LAST:event_btnDeletarActionPerformed

    private void txtFiltrarKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtFiltrarKeyReleased
        if (evt.getKeyCode() == KeyEvent.VK_ENTER) {
            if (!txtFiltrar.getText().trim().equals("") || txtFiltrar.getText().equals("Pesquisar...")) {
                int op = selFiltrar.getSelectedIndex();
                switch (op) {
                    case 0:
                        readJTableForID(txtFiltrar.getText());
                        break;
                    case 1:
                        readJTableForNome(txtFiltrar.getText());
                        break;
                    case 2:
                        readJTableForSobrenome(txtFiltrar.getText());
                        break;
                    case 3:
                        readJTableForCelular(txtFiltrar.getText());
                        break;
                    case 4:
                        readJTableForEmail(txtFiltrar.getText());
                        break;
                    case 5:
                        readJTableForRegiao(txtFiltrar.getText());
                        break;
                }
            } else {
                readJTable();
            }
        }
    }//GEN-LAST:event_txtFiltrarKeyReleased

