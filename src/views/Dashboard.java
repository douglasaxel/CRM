    private void jTable1MouseReleased(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseReleased
        if (jTable1.getSelectedRow() != -1) {
            ClienteDAO dao = new ClienteDAO();
            List<Cliente> cl = dao.search_by_id(jTable1.getValueAt(jTable1.getSelectedRow(), 0).toString());
            Cliente c = cl.get(0);

            tfNomeSobre.setText(c.getNome() + " " + c.getSobrenome());
            ftCPF.setText(c.getCpf());
            tfDataNasc.setText(c.getData_nasc());
            tfEndereco.setText(c.getEndereco());
            tfBairro.setText(c.getBairro());
            tfRegiao.setText(c.getRegiao());
            ftTelefone.setText(c.getTelefone());
            ftCelular.setText(c.getCelular());
            tfEmail.setText(c.getEmail());
            jTextArea1.setText(c.getDescricao());
        }
    }//GEN-LAST:event_jTable1MouseReleased

